package assignment;
import java.util.*;
public class Type_cast {
public static void main(String args[]) {
				Scanner sc = new Scanner(System.in);
				System.out.println("input a number");
				int a = sc.nextInt();
				System.out.println("Typecasting it to Integer: "+ a);
				System.out.println("Typecasting it to Float: "+(float)a);
				System.out.println("Typecasting it to: "+(long)a);
				System.out.println("Typecasting it to short: " +(short)a);
				System.out.println("Typecasting it to double: " +(double)a);
			}
}

